const STATE_CODE_SET = new Set(US_STATES.map((s) => s.code));

export function isValidStateCode(code: string): boolean {
  return STATE_CODE_SET.has(code.toUpperCase());
}

export function normalizeStateCodes(codes: string[] | null | undefined): string[] {
  if (!codes?.length) return [];
  return Array.from(
    new Set(
      codes
        .map((c) => c.trim().toUpperCase())
        .filter((c) => isValidStateCode(c))
    )
  ).sort();
}

export function formatStateList(codes: string[]): string {
  const normalized = normalizeStateCodes(codes);
  if (normalized.length === 0) return 'None selected';
  return normalized
    .map((code) => US_STATES.find((s) => s.code === code)?.name ?? code)
    .join(', ');
}